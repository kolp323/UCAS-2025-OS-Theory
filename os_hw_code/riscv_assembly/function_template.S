# 1. 准备参数
    li a0, 5                # 将 5 放入第一个参数寄存器 a0
    li a1, 7                # 将 7 放入第二个参数寄存器 a1

    # 2. 执行调用
    jal add_numbers         # 跳转并调用 add_numbers 函数

    # 3. 处理返回值
    mv t0, a0               # 将 a0 (返回值) 里的 12 移动到 t0
    
    # 程序从这里继续，t0 里的值现在是 12

add_numbers:
    # --- 函数序言 (Prologue) ---
    # 保存 s0 寄存器，因为它是一个“保存”寄存器
    addi sp, sp, -4        # 1. 调整栈指针，腾出空间
    sw s0, 0(sp)           # 2. 将 s0 的值存储到栈上

    # --- 函数体 ---
    # 假设需要使用 s0 来进行计算
    addi s0, a0, 5         # s0 = a0 + 5
    add a0, s0, a1         # a0 = s0 + a1，结果存回 a0

    # --- 函数尾声 (Epilogue) ---
    # 恢复 s0 寄存器
    lw s0, 0(sp)           # 1. 从栈上加载数据回 s0
    addi sp, sp, 4         # 2. 恢复栈指针，增加 4

    # --- 返回指令 ---
    ret                    # 返回到调用者的代码